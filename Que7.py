"""Arithmatic Operators--  Addition(+), Subtraction(-), Multiplication(*), Division(/), Floor division(//), modulos(%),
Power(**)"""

a = 10
b = 3
print("a+b = ",a+b)
print("a-b = ", a-b)
print("a*b = ",a*b)
print("a/b = ",a/b)
print("a//b = ",a//b)
print("a%b = ",a%b)
print("a**b = ",a**b)
